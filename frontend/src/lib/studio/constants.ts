/**
 * Spacing (in Babylon units) between characters in the 3D viewer.
 * Increase to spread characters further apart; decrease to bring them closer.
 */
export const CHARACTER_SPACING = 6;

/**
 * Baseline Y (height) for character placement. Characters are placed this far
 * above ground level (y=0) to avoid clipping or sinking into the floor.
 */
export const CHARACTER_BASELINE_Y = 0.5;

/**
 * Depth spacing (Z axis) between characters. Adds depth variation so they
 * don't all line up on a single plane.
 */
export const CHARACTER_DEPTH_SPACING = 2;
